		<h3>รายละเอียดหลักสูตร</h3>
<p>หลักสูตรกำลังดำเนินการจัดเตรียมโดย TCDC</p>
<p>TCDC ได้ส่งเสริมการออกแบบบริการ (Service Design) ในประเทศไทยมาตั้งแต่ปี 2555 โดยการจัดการบรรยาย อบรมสัมมนาเชิงปฏิบัติการ และการอบรมผ่านการทําโครงการ (Project-based)  ในขณะที่การออกแบบบริการเป็นที่รู้จักและต้องการมากขึ้นในประเทศไทย บุคลากรด้านการออกแบบบริการยังมีจำนวนจำกัดมาก TCDC เล็งเห็นความจำเป็นในการอบรมนักออกแบบบริการและเพิ่มพูนความสามารถของบุคลากร เพื่อรองรับความต้องการที่มีเพิ่มสูงขึ้นในธุรกิจไทย TCDC จึงได้จัดทำหลักสูตรการออกแบบบริการเพื่อเพิ่มศักยภาพของผู้ดำเนินการ (Facilitator) และที่ปรึกษา (Consultant) ด้านการออกแบบบริการ ให้สามารถประยุกต์ใช้องค์ความรู้นี้ในธุรกิจของตนหรือในภาคอุตสาหกรรมต่างๆ อย่างมีประสิทธิภาพ</p>
<h3>วัตถุประสงค์ของหลักสูตร</h3>
<ol>
 	<li>
<p>เพื่อสอนและประยุกต์ใช้การออกแบบบริการให้เหมาะสมกับบริบทและความต้องการในประเทศไทย</p>
</li>
 	<li>
<p>เพื่อสร้างผู้สนับสนุน (Advocate) ผู้ดำเนินการ (Facilitator) และบุคลากรด้านการออกแบบบริการที่มีคุณภาพ ซึ่งสามารถประสานงานระหว่างผู้เชี่ยวชาญในศาสตร์ต่างๆ และผู้ที่เกี่ยวข้องในโครงการ (บุคลากรเหล่านี้จะสามารถประเมินคุณภาพของข้อมูล ผลการออกแบบบริการ และคุณภาพของที่ปรึกษาโครงการได้)</p>
</li>
 	<li>
<p>เพื่อทำให้ผู้ที่เกี่ยวข้อง (Stakeholders) คุ้นเคยและมีประสบการณ์ตรงผ่านการมีส่วนร่วมในการออกแบบบริการขั้นตอนต่างๆ (Project-based Activities) ผ่านโครงการจริง</p>
</li>
</ol>
<h3>ประโยชน์ที่ได้รับจากหลักสูตรการออกแบบบริการ TCDC</h3>
<p>หลักสูตรการออกแบบบริการ TCDC และแบบเครื่องมือการออกแบบบริการ (Service Design Template) นี้ สามารถใช้ต่อยอดในการจัดการอบรมและการเผยแพร่ความรู้ในลักษณะต่างๆ ดังต่อไปนี้</p>
<ul>
 	<li>
<p>การอบรมระยะสั้นเพื่อแนะนำเรื่องการออกแบบบริการ (Short Course: Intro to Service Design)</p>
</li>
 	<li>
<p>การอบรมเพื่อการออกแบบบริการสาธารณะ (Public Services)</p>
</li>
 	<li>
<p>การอบรมสำหรับองค์กรผู้ผลิต (Manufacturing Organization)</p>
</li>
 	<li>
<p>การอบรมการออกแบบบริการสำหรับนักออกแบบ (Service Design for Designers)</p>
</li>
 	<li>
<p>โครงการอบรมและการเรียนการสอนในมหาวิทยาลัย</p>
</li>
 	<li>
<p>การเผยแพร่องค์ความรู้เรื่องการออกแบบบริการผ่าน TCDC ในส่วนภูมิภาคต่างๆ</p>
</li>
 	<li>
<p>การเผยแพร่องค์ความรู้เรื่องการออกแบบบริการไปยังองค์กรต่างๆ ในประเทศไทย</p>
</li>
</ul>


				
